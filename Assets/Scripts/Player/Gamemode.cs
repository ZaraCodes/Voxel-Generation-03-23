public enum Gamemode
{
    /// <summary>
    /// Gamemode where players experience hunger, take damage from enemies and the environment, do combat, gather materials and can place and break blocks
    /// </summary>
    Survival,
    /// <summary>
    /// Infinite supply of blocks and items, no hunger and damage and the player can fly
    /// </summary>
    Creative,
    /// <summary>
    /// The player can phase through the world without any collision
    /// </summary>
    Spectator
}
